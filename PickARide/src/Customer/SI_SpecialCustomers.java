    private void jBtnPRINTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPRINTActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=PickARide;selectMethod=cursor", "sa", "123456");

            //System.out.println("DATABASE NAME IS:" + connection.getMetaData().getDatabaseProductName());

            Statement statement = connection.createStatement();
            ResultSet resultSet = statement
            .executeQuery("SELECT CustomerId, CustomerName, C_Phone, C_EmailAddress "
                    + "FROM CUSTOMER WHERE CustomerId IN(SELECT CustomerId  FROM ORDER_FORM"
                    + " GROUP BY CustomerId "
                    + "HAVING COUNT(CustomerId)>=2 )");

            while (resultSet.next()) {
                
                String CustomerID = resultSet.getString("CustomerId");
                String CustomerName = resultSet.getString("CustomerName");
                String C_Phone = resultSet.getString("C_Phone");
                String C_Email = resultSet.getString("C_EmailAddress");
                
                String tbData[]={CustomerID,CustomerName, C_Phone, C_Email};

                DefaultTableModel tblModel = (DefaultTableModel)jTableCustomerBonus.getModel();
                tblModel.addRow(tbData);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,0);
        }
    }//GEN-LAST:event_jBtnPRINTActionPerformed

