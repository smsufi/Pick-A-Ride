    private void jBtnPRINTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPRINTActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=PickARide;selectMethod=cursor", "sa", "123456");

            //System.out.println("DATABASE NAME IS:" + connection.getMetaData().getDatabaseProductName());

            Statement statement = connection.createStatement();
            ResultSet resultSet = statement
            .executeQuery("SELECT * FROM BIKE_FINANCE");

            while (resultSet.next()) {
                String BFinSerialNo = resultSet.getString("BFinSerialNo");
                String BikeId = resultSet.getString("BikeId");
                String EngineCost = resultSet.getString("EngineCost");
                String FuelCost = resultSet.getString("FuelCost");
                String TierCost = resultSet.getString("TierCost");
                String TotalCost = resultSet.getString("TotalCost");

                String tbData[]={BFinSerialNo, BikeId,EngineCost,FuelCost,
                    TierCost,TotalCost};

                DefaultTableModel tblModel = (DefaultTableModel)jTableBikeFinance.getModel();
                tblModel.addRow(tbData);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBtnPRINTActionPerformed

