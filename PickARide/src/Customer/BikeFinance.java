    private void jBtnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDELETEActionPerformed
        // TODO add your handling code here:

        DefaultTableModel tblModel = (DefaultTableModel)jTableBikeFinance.getModel();

        //delete row
        if(jTableBikeFinance.getSelectedRowCount()==1){
            try
            {
                String query = "delete from BIKE_FINANCE where BFinSerialNo=?";

                PreparedStatement pst = connection.prepareStatement(query);

                pst.setString(1,jTextBFinSerialNo.getText());

                pst.executeUpdate();
                tblModel.removeRow(jTableBikeFinance.getSelectedRow());
                JOptionPane.showMessageDialog (this,"Data has been deleted");

            } catch(Exception e){
                JOptionPane.showMessageDialog (this, e.getMessage());
            }
        }
        else {

            if(jTableBikeFinance.getRowCount()==0)
            JOptionPane.showMessageDialog (this, "Table is empty");

            else
            JOptionPane.showMessageDialog (this, "Please select single row");
        }
        jTextBFinSerialNo.setText("");
        jTextBikeID.setText("");
        jTextEngineCost.setText("");
        jTextFuelCost.setText("");
        jTextTierCost.setText("");
        jTextTotalCost.setText("");
    }//GEN-LAST:event_jBtnDELETEActionPerformed

