    private void jBtnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUPDATEActionPerformed
        // TODO add your handling code here:

        DefaultTableModel tblModel = (DefaultTableModel)jTableBikeFinance.getModel();

        if(jTableBikeFinance.getSelectedRowCount()==1){
            String BFinSerialNo = jTextBFinSerialNo.getText();
            String BikeID = jTextBikeID.getText();
            String EngineCost = jTextEngineCost.getText();
            String FuelCost = jTextFuelCost.getText();
            String TierCost = jTextTierCost.getText();
            String TotalCost = jTextTotalCost.getText();

            try
            {

                String query = "update BIKE_FINANCE set BikeId=?,EngineCost=?,FuelCost=?,TierCost=?,TotalCost=? where BFinSerialNo = ?";

                PreparedStatement pst = connection.prepareStatement(query);

                pst.setString(1,jTextBikeID.getText());
                pst.setString(2,jTextEngineCost.getText());
                pst.setString(3,jTextFuelCost.getText());
                pst.setString(4,jTextTierCost.getText());
                pst.setString(5,jTextTotalCost.getText());
                pst.setString(6,jTextBFinSerialNo.getText());

                pst.executeUpdate();
                //set updated value to table row
                tblModel.setValueAt(BFinSerialNo,jTableBikeFinance.getSelectedRow(),0);
                tblModel.setValueAt(BikeID,jTableBikeFinance.getSelectedRow(),1);
                tblModel.setValueAt(EngineCost,jTableBikeFinance.getSelectedRow(),2);
                tblModel.setValueAt(FuelCost,jTableBikeFinance.getSelectedRow(),3);
                tblModel.setValueAt(TierCost,jTableBikeFinance.getSelectedRow(),4);
                tblModel.setValueAt(TotalCost,jTableBikeFinance.getSelectedRow(),5);

                JOptionPane.showMessageDialog (this,"Update is successful");

                jTextBFinSerialNo.setText("");
                jTextBikeID.setText("");
                jTextEngineCost.setText("");
                jTextFuelCost.setText("");
                jTextTierCost.setText("");
                jTextTotalCost.setText("");

            } catch(Exception e){
                JOptionPane.showMessageDialog (this, e.getMessage());
            }

            //update message
            //JOptionPane.showMessageDialog (this, "Update is successful");
        }
        else{
            if(jTableBikeFinance.getRowCount()==0)
            JOptionPane.showMessageDialog (this, "Table is empty");
            else
            JOptionPane.showMessageDialog (this, "Please select single row");

        }
    }//GEN-LAST:event_jBtnUPDATEActionPerformed

